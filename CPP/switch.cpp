//use of switch statement.

#include <iostream>
using namespace std;

int main()
{int x;
  cout << "Enter the value of x: ";
  cin >> x;

  switch (x)
    {case 1:
	cout << "x is 1." << endl;
	

    case 2:
       cout << "x is 2." << endl;
       break;

    default:
      cout << "x is unknown!" << endl;
      
      
       
    }
}
